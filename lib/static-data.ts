// Helper: Parse copy-pasted Excel strings and expand to 99 months
const parse = (raw: string): Matrix => {
  return raw
    .trim()
    .split('\n')
    .map((line) => {
      // Remove "风控N" labels at start
      const content = line.replace(/^.*?风控\d+\s+/, '').trim();
      const tokens = content.split(/\t+/);

      const row = tokens.map((t) => {
        const clean = t.replace(/[¥,]/g, '');
        if (clean.includes('%')) {
          return Number.parseFloat(clean.replace('%', '')) / 100;
        }
        return Number.parseFloat(clean);
      });

      // Pad to 99 months by repeating the last value
      while (row.length < 99) row.push(row[row.length - 1]);
      return row.slice(0, 99);
    });
};

